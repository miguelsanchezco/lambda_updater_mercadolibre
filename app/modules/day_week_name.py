def day_week_name(day_number):
  if day_number == 0:
    day_name = 'Lunes'
  elif day_number == 1:
    day_name = 'Martes'
  elif day_number == 2:
    day_name = 'Miercoles'
  elif day_number == 3:
    day_name = 'Jueves'
  elif day_number == 4:
    day_name = 'Viernes'
  elif day_number == 5:
    day_name = 'Sabado'
  elif day_number == 6:
    day_name = 'Domingo'
  return day_name